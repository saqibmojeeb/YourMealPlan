export const getRecipesByType = (type: string): Recipe[] => {
  return mockRecipes.filter(recipe => recipe.type === type);
};

export const getRecipeById = (id: string): Recipe | undefined => {
  return mockRecipes.find(recipe => recipe.id === id);
};

export const getFilteredRecipesByType = (type: string, dietaryPreferences: string[]): Recipe[] => {
  let recipes = getRecipesByType(type);
  
  if (dietaryPreferences.length === 0) return recipes;

  // Map dietary preferences to tag filters
  const tagMappings: Record<string, (tags: string[]) => boolean> = {
    'vegetarian': (tags) => tags.includes('vegetarian') || tags.includes('vegan'),
    'vegan': (tags) => tags.includes('vegan'),
    'gluten-free': (tags) => tags.includes('gluten-free'),
    'dairy-free': (tags) => tags.includes('dairy-free') || tags.includes('vegan'),
    'keto': (tags) => tags.includes('low-carb'),
    'mediterranean': (tags) => tags.includes('mediterranean'),
  };

  return recipes.filter(recipe => {
    return dietaryPreferences.every(pref => {
      const check = tagMappings[pref];
      return check ? check(recipe.tags) : true;
    });
  });
};
